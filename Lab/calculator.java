class calculator {
    public static void main(String[] args) {
    
        int a = 10;
        int b = 5;
        // arithmetic operators
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("a ^ b = " + (a ^ b));
        System.out.println("a & b = " + (a & b));
        System.out.println("a | b = " + (a | b));
    

        // relational operators
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));


      
    }
}